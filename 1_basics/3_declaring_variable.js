/**
 * Declaring variables
 * 
 * 1. var - no more use
 * 2. let 
 * 3. const 
 */
var name = 'code factory';
console.log(name);

var age = 32;

let ive = 'ive';
console.log(ive);

/**
 * let and var 
 * 나중에 값을 바꿀수 있다.
 */

const newJeans = 'NewJeans';
console.log(newJeans);

// newJeans = 'fact'

/**
 * 선언과 할당 
 * declaration assignment
 * 
 * 1. 변수는 선언하는 것
 * 2. 할당 (값을 넣어주는 과정)
 * 
 */
var name = 'code';
console.log(name);

let girlFriend;
